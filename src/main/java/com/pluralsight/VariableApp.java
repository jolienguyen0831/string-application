package com.pluralsight;

public class VariableApp {
    public static void main(String[] args) {

        //Step 1
        String favoriteColor = "blue";
        System.out.println("My favorite color is: " + favoriteColor);
        short classYear = 2026;
        System.out.println("My class year at YUU is: " + classYear);
        String middleInitial = "NP";
        System.out.println("My middle initial is: " + middleInitial);
        boolean havePets = false;
        System.out.println("Have pets? " + havePets);
        String niceMessage = "Beautiful day!";
        System.out.println("Message: " + niceMessage);

        //Step 2
        byte numberDaysInAWeek = 7;
        System.out.println("Number of days in a week is: " + numberDaysInAWeek);
        float priceOfCoffee = 4.99f;
        System.out.println("Price of coffee is: " + priceOfCoffee);
        char myFavoriteLetter = 'U';
        System.out.println("My favorite letter is: " + myFavoriteLetter);
        boolean isRaining = false;
        System.out.println("Is raining? " + isRaining);



    }
}
